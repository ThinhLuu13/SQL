CREATE PROC SP_getTableList
	@arrTables CURSOR VARYING OUTPUT 
AS 
BEGIN
	SET @arrTables = CURSOR FOR
		SELECT table_name
		FROM Information_Schema. TABLES
		WHERE table_type='BASE TABLE'
	OPEN @arrTables
END
GO

IF EXISTS (SELECT * FROM SYSOBJECTS WHERE ID = OBJECT_ID(N'SP_GetFKList') AND TYPE = 'P')
	DROP PROC SP_GetFKList
GO
CREATE PROC SP_GetFKList
	@arrFKs CURSOR VARYING OUTPUT  
AS
BEGIN 
	SET @arrFKs = CURSOR FOR
		SELECT constraint_name, table_name
		FROM Information_Schema. TABLE_CONSTRAINTS
		WHERE constraint_type='FOREIGN KEY'
	OPEN @arrFKs
END
GO

IF EXISTS (SELECT * FROM SYSOBJECTS WHERE ID = OBJECT_ID(N'SP_ClearFKs') AND TYPE = 'P')
	DROP PROC SP_ClearFKs
GO
CREATE PROC SP_ClearFKs
	@dbName varchar(66)
AS 
BEGIN
	
	DECLARE @curDB varchar(66)
	SET @curDB=db_name() 
		
	IF (@dbName is null) or (@dbName='') BEGIN
		SET @dbName=db_name()
	END
	IF @dbName in ('master') BEGIN
		RAISERROR ('Không thể xóa object(s) hệ thống!',16,1)
		RETURN -1
	END
	DECLARE @sc nvarchar(333)
	
	SET @sc='use '+@dbName
	EXEC sp_executeSQL @sc

	DECLARE @c CURSOR 
	EXEC SP_GetFKList @c OUTPUT

	DECLARE @tableName varchar(66), @fkName varchar(66)
	FETCH NEXT FROM @c INTO @fkName, @tableName
	WHILE @@fetch_status=0 BEGIN
		SET @sc='alter table '+@tableName+' drop constraint '+ @fkName
		EXEC sp_executeSQL @sc
		FETCH NEXT FROM @c INTO @fkName, @tableName
	END
	CLOSE @c
	DEALLOCATE @c
	
	SET @sc='use '+@curDB
	EXEC sp_executeSQL @sc
END
GO


IF EXISTS (SELECT * FROM SYSOBJECTS WHERE ID= OBJECT_ID(N'SP_ClearTables') and TYPE = 'P')
	DROP PROC SP_ClearTables
GO
CREATE PROC SP_ClearTables
	@dbName varchar(66)
AS 
BEGIN
	DECLARE @curDB varchar(66)
	SET @curDB=db_name()
	
	DECLARE @sc nvarchar(333)

	IF (@dbName is null) or (@dbName='') BEGIN
		SET @dbName=db_name()
	END
	IF @dbName in ('master') BEGIN
		RAISERROR ('Không thể xóa object(s) hệ thống!',16,1)
		RETURN -1
	END
		
	SET @sc='use '+@dbName
	EXEC sp_executeSQL @sc
			
	DECLARE @c CURSOR
	EXEC SP_getTableList @c OUTPUT

	DECLARE @tableName varchar(66)
	FETCH NEXT FROM @c INTO @tableName
	WHILE @@fetch_status=0 BEGIN
		SET @sc='drop table '+@tableName
		EXEC sp_executeSQL @sc
		
		FETCH NEXT FROM @c INTO @tableName
	END
	CLOSE @c
	DEALLOCATE @c
	
	SET @sc = 'use '+@curDB
	EXEC sp_executeSQL @sc
	
	RETURN 0
END
GO


IF EXISTS (SELECT * FROM SYSOBJECTS WHERE ID=OBJECT_ID(N'SP_ClearDB') AND TYPE = 'P') 
	DROP PROC SP_ClearDB
GO
CREATE PROC SP_ClearDB
	@dbName varchar(66)
AS
BEGIN
	DECLARE @curDB varchar(66)
	SET @curDB=db_name()
	
	IF (@dbName is null) or (@dbName='') BEGIN
		SET @dbName=db_name()
	END

	IF @dbName in ('master') BEGIN
		RAISERROR ('Không thể xóa object(s) hệ thống!',16,1)
		RETURN -1
	END
	
	EXEC SP_ClearFKs @dbName
	EXEC SP_ClearTables @dbName
	
	DECLARE @sc nvarchar(333)
	SET @sc = 'use '+@curDB
	EXEC sp_executeSQL @sc
	
	RETURN 0
END
GO
